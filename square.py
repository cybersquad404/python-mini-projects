import turtle
def draw_square():
    window = turtle.Screen()
    window.bgcolor("red")

    ashish = turtle.Turtle()
    ashish.forward(100)
    ashish.right(90)
    ashish.forward(100)
    ashish.right(90)
    ashish.forward(100)
    ashish.right(90)
    ashish.forward(100)
    ashish.right(90)
    window.exitonclick()

draw_square()
